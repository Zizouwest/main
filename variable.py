dictionnaire = {'cle1': 'valeur1', 'cle2': 'valeur2'}  # dict (dictionnaire)

# Affichage des valeurs de chaque variable
print("Valeur de l'entier :", entier)
print("Valeur du flottant :", flottant)
print("Valeur de la chaîne :", chaine)
print("Valeur du booléen :", booleen)
print("Valeur de la liste :", liste)
print("Valeur du tuple :", tuple_)
print("Valeur du dictionnaire :", dictionnaire)
