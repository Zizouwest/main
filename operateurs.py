a = 16
b = 3

# Calculs de base
add = a + b
soustraction = a - b
multiplication = a * b
division = a / b  # Division (résultat en float)
modulo = a % b

# Affichage des résultats des calculs de base
print("L'addition est égale à :", add)
print("La soustraction est égale à :", soustraction)
print("La multiplication est égale à :", multiplication)
print("La division est égale à :", division)
print("Le modulo est égal à :", modulo)

# Comparaison de 'a' et 'b'
if a == b:
    print("La condition est vraie :", True)
else:
    print("La condition est fausse :", False)

# Vérification si 'a' est supérieur à 'b'
if a > b:
    print("La condition est vraie :", True)
else:
    print("La condition est fausse :", False)
